export function getZiCalendar(date: Date): ZiCalendar | undefined {
  const mm = String(date.getMonth() + 1).padStart(2, "0");
  const dd = String(date.getDate()).padStart(2, "0");
  return calendarOrtodox[`${mm}-${dd}`];
}

export function getSfintiiZilei(date: Date): string {
  const zi = getZiCalendar(date);
  return zi ? zi.sfinti[0] ?? "Zi obișnuită" : "Zi obișnuită";
}
